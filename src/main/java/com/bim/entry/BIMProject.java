package com.bim.entry;

public class BIMProject {
    String name;
    String Intro;
    String Id;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIntro() {
        return Intro;
    }

    public void setIntro(String intro) {
        Intro = intro;
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }
}